<script>
import { ref, onMounted } from 'vue'
import { authFetch } from '@/utils/api'

export default {
  name: 'Stats',
  setup() {
    const loading = ref(false)
    const lastUpdate = ref('')

    const stats = ref({
      todayRequests: 0,
      totalRequests: 0,
      successRate: 0,
      onlineWorkers: 0,
      totalWorkers: 0,
      avgResponseTime: 0,
      blockedIPs: 0,
      todayBlocked: 0,
      violationRequests: 0,
      memoryUsage: 0,
      cpuUsage: 0,
      uptime: '0分钟'
    })

    // Worker限制数据
    const workerLimits = ref(null)

    // 日志相关
    const logs = ref([])
    const selectedLogType = ref('all')

    const refreshStats = async () => {
      loading.value = true
      try {
        // 调用统计数据API
        const response = await authFetch('/api/stats/summary')
        if (response.ok) {
          const data = await response.json()
          stats.value = {
            todayRequests: data.todayRequests || 0,
            totalRequests: data.totalRequests || 0,
            successRate: data.successRate || 0,
            onlineWorkers: data.onlineWorkers || 0,
            totalWorkers: data.totalWorkers || 0,
            avgResponseTime: data.avgResponseTime || 0,
            blockedIPs: data.blockedIPs || 0,
            todayBlocked: data.todayBlocked || 0,
            violationRequests: data.violationRequests || 0,
            memoryUsage: data.memoryUsage || 0,
            cpuUsage: data.cpuUsage || 0,
            uptime: data.uptime || '0分钟'
          }
          lastUpdate.value = new Date().toLocaleString()
        } else {
          throw new Error(`API调用失败: ${response.status}`)
        }
      } catch (error) {
        console.error('刷新统计数据失败:', error)
        // 如果API调用失败，保持当前数据不变
        lastUpdate.value = new Date().toLocaleString()
      } finally {
        loading.value = false
      }
    }

    // 刷新Worker限制数据
    const refreshWorkerLimits = async () => {
      try {
        const response = await authFetch('/api/web-config/worker/stats')
        if (response.ok) {
          const data = await response.json()
          if (data.success && data.stats && data.stats.rate_limit_stats) {
            workerLimits.value = data.stats.rate_limit_stats
          }
        }
      } catch (error) {
        console.error('获取Worker限制数据失败:', error)
      }
    }

    // 加载日志
    const loadLogs = async () => {
      try {
        let url = '/api/logs'
        if (selectedLogType.value === 'worker') {
          url = '/api/logs/worker-logs'
        }

        const response = await authFetch(url)
        if (response.ok) {
          const data = await response.json()
          logs.value = data.logs || []
        }
      } catch (error) {
        console.error('加载日志失败:', error)
        logs.value = []
      }
    }

    // 格式化时间
    const formatTime = (timeStr) => {
      if (!timeStr) return ''
      try {
        return new Date(timeStr).toLocaleString()
      } catch {
        return timeStr
      }
    }

    onMounted(() => {
      refreshStats()
      refreshWorkerLimits()
      loadLogs()
    })

    return {
      stats,
      loading,
      lastUpdate,
      refreshStats,
      workerLimits,
      refreshWorkerLimits,
      logs,
      selectedLogType,
      loadLogs,
      formatTime
    }
  }
}
</script>

